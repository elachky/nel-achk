#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	int		i;

	i = ft_strlen((char *)s1);
	if (!(p = ft_calloc(1, i + 1)))
		return (0);
	while (i--)
		p[i] = (char)s1[i];
	return (p);
}
