#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int p;

	p = 0;
	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
		if (s[i--] == c)
			return ((char *)s + (i + 1));
	return (0);
}
