#include <ft_printf.h>

void	ft_putnbr(unsigned int n)
{
	long nb;

	nb = n;
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putchar_fd(nb % 10 + '0', 1);
	}
	else
		ft_putchar_fd('0' + nb, 1);
}
