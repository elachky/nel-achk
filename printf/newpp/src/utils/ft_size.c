int	ft_size(int n)
{
	long	tmp;
	int		size;

	tmp = n;
	size = 0;
	if (tmp < 0)
		tmp = -tmp;
	while (tmp > 0)
	{
		size++;
		tmp = tmp / 10;
	}
	if (n <= 0)
		size++;
	return (size);
}
